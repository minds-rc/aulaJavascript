// operador de atribuição (=)
let idadeDanilo = 33;
let idadeLaurindo = 22;
let nome1 = "Danilo"
let nome2 = "Laurindo"

// operadores numéricos (matemáticos)
console.log(idadeDanilo + idadeLaurindo + " | soma de dus variáveis") // soma
console.log(idadeDanilo - idadeLaurindo + " | suntração de duas variáveis") // subtração
console.log(idadeDanilo * idadeLaurindo + " | multiplicação de duas variáveis") // multiplicação
console.log(idadeDanilo / idadeLaurindo + " | divisão de duas variáveis") // divisão
console.log(idadeDanilo % idadeLaurindo + " | resto da divisão de duas variáveis") // resto da divisão

//operador concatenação
console.log('----------------------------------------')
console.log(nome1 + ' ' + idadeDanilo + ' anos')
console.log(nome2 + ' ' + idadeLaurindo + ' anos')